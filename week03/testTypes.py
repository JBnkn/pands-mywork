# learning about variable types in Python

int = 7
float = 5.83
boolean = False
str = "this is a string"
list = ["sunday", "monday", "happy_days"]

print("variable {} is of type: {} and value: {}".format("int", type(int), int))
print("variable {} is of type: {} and value: {}".format("float", type(float), float))
print("variable {} is of type: {} and value: {}".format("boolean", type(boolean), boolean))
print("variable {} is of type: {} and value: {}".format("str", type(str), str))
print("variable {} is of type: {} and value: {}".format("list", type(list), list))